import os
import json
import random
import numpy as np
import torch
import librosa
import logging
from torch.utils.data import Dataset

logger = logging.getLogger(__name__)

_SYSTEM_PROMPT = (
    "You are an expert audio analyst. Listen carefully to the audio and "
    "describe sound events accurately, including their temporal order "
    "(what happens first, next, and last)."
)


class AudioQADataset(Dataset):
    """
    Dataset for AudioCaps-based Audio Question Answering fine-tuning.

    Expected JSON format (generated by scripts/prepare_audiocaps.py):
    [
        {
            "audio": "audiocaps_<youtube_id>_<start>",  # no .wav extension
            "prompt": "Describe the temporal order of events...",
            "answer": "First there is rain, then thunder...",
            "choices": ["A", "B", "C", "D"]             # optional, for MCQ
        }
    ]
    """

    def __init__(
        self,
        data_path: str,
        audio_root: str,
        processor,
        max_length: int = 1024,
        split: str = "train",
        augment_prompts: bool = True,
    ):
        self.processor       = processor
        self.audio_root      = audio_root
        self.max_length      = max_length
        self.split           = split
        self.augment_prompts = augment_prompts
        self.sr              = processor.feature_extractor.sampling_rate

        with open(data_path, "r") as f:
            self.samples = json.load(f)

        # Pre-filter samples whose audio file exists to avoid repeated I/O
        valid = []
        for s in self.samples:
            p = os.path.join(audio_root, s["audio"] + ".wav")
            if os.path.exists(p):
                valid.append(s)
            else:
                logger.debug(f"Skipping missing audio: {p}")
        if len(valid) < len(self.samples):
            logger.warning(
                f"Skipped {len(self.samples) - len(valid)} samples with missing audio "
                f"({len(valid)} remaining)."
            )
        self.samples = valid
        logger.info(f"[{split}] Loaded {len(self.samples)} valid samples from {data_path}")

    def __len__(self):
        return len(self.samples)

    def _build_conversation(self, sample: dict) -> tuple:
        audio_path = os.path.join(self.audio_root, sample["audio"] + ".wav")

        if "choices" in sample:
            joined = "\n".join(f"  {chr(65+i)}. {c}" for i, c in enumerate(sample["choices"]))
            prompt_text = (
                f"{sample['prompt']}\n\nChoices:\n{joined}\n\n"
                "Reply with ONLY the letter and text of the correct choice."
            )
        else:
            prompt_text = sample["prompt"]

        conversation = [
            {
                "role": "system",
                "content": [{"type": "text", "text": _SYSTEM_PROMPT}],
            },
            {
                "role": "user",
                "content": [
                    {"type": "audio", "audio": audio_path},
                    {"type": "text",  "text": prompt_text},
                ],
            },
            {
                "role": "assistant",
                "content": [{"type": "text", "text": sample["answer"]}],
            },
        ]
        return conversation, audio_path

    def __getitem__(self, idx: int) -> dict:
        sample     = self.samples[idx]
        audio_path = os.path.join(self.audio_root, sample["audio"] + ".wav")

        try:
            audio, _ = librosa.load(audio_path, sr=self.sr, mono=True)
        except Exception as e:
            logger.warning(f"Failed to load audio {audio_path}: {e} — using silence")
            audio = np.zeros(self.sr, dtype=np.float32)  # 1 second of silence

        conversation, _ = self._build_conversation(sample)

        prompt_str = self.processor.apply_chat_template(
            conversation,
            tokenize=False,
            add_generation_prompt=False,
        )

        return {
            "prompt":     prompt_str,
            "audio":      audio,
            "audio_path": audio_path,
            "answer":     sample["answer"],
        }
